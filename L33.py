import turtle

def draw_tri(x):
    fred=turtle.Turtle()
    fred.color(x)
    fred.forward(200)
    fred.right(90)
    fred.forward(200)
    fred.right(90)
    fred.forward(200)
    fred.right(90)
    fred.forward(200)



draw_tri("blue")
draw_tri("green")
draw_tri("cyan")
draw_tri("gold")
